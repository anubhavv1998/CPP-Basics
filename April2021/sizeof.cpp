/*checking size of all data types in cpp
It may vary from compiler to compiler*/
#include<iostream>
using namespace std;
int main(){
    cout<<"Size of boolean in byte is "<<sizeof(bool)<<endl;
    cout<<"Size of char in byte is "<<sizeof(char)<<endl;
    cout<<"Size of unsigned char in byte is "<<sizeof(unsigned char)<<endl;
    cout<<"Size of int in byte is "<<sizeof(int)<<endl;
    cout<<"Size of long int in byte is "<<sizeof(long int)<<endl;
    cout<<"Size of long long in byte is "<<sizeof(long long int)<<endl;
    cout<<"Size of unsigned int in byte is "<<sizeof(unsigned int)<<endl;
    cout<<"Size of float in byte is "<<sizeof(float)<<endl;
    cout<<"Size of double in byte is "<<sizeof(double)<<endl;
    
}